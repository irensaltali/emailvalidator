        public async Task<bool> Validate(string email)
        {

            MailAddress address;

            //1 Email address syntax check
            try
            {
                address = new MailAddress(email);
            }
            catch
            {
                return false;
            }

            //2 Disposable email address (DEA) detection
            if (DisposableDomains.Where(x => x == address.Host).Count() > 0)
                return false;

            //3 Role-based account detection
            if (EmailRoles.Where(x => x == address.User).Count() > 0)
                return false;


            //4 MX record check
            var lookup = new LookupClient();
            var result = await lookup.QueryAsync(address.Host, QueryType.MX);
            if (result.HasError)
                return false;

            //5 SMTP check
            bool valid = false;
            foreach (var record in result.AllRecords)
                if (ValidSMTP(record.DomainName.Value))
                    valid = true;

            return valid;
        }


        private bool ValidSMTP(string hostName)
        {
            bool valid = false;
            try
            {
                TcpClient smtpTest = new TcpClient
                {
                    ReceiveTimeout = 5,
                    SendTimeout = 5,
                };
                smtpTest.Connect(hostName, 25);
                if (smtpTest.Connected)
                {
                    NetworkStream ns = smtpTest.GetStream();
                    StreamReader sr = new StreamReader(ns);
                    if (sr.ReadLine().Contains("220"))
                    {
                        valid = true;
                    }
                    smtpTest.Close();
                }
            }
            catch
            {
                return false;
            }
            return valid;
        }

